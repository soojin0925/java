package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		String 이름 = "홍길동";
		int 출생연도 = 2000;
		double 키 = 175.5;  		// 변수명은 원하는..(알아볼수있게)
		
		
		System.out.println("이름 : " + 이름);
		System.out.println("출생연도 : "+출생연도+ "년");
		System.out.println("키 : " +키 +"cm");	
	}
}
